/** Оноотой асуулт үүсгэх — сонголтуудыг scores-ийн дарааллаар нь авна. */
function scored(
  base: Omit<Question, "options" | "scores">,
  scores: Record<string, number>,
): Question {
  return { ...base, options: Object.keys(scores), scores };
}

